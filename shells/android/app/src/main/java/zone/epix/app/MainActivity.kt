/**
 * The Android shell: a browser over the embedded Epix node.
 *
 * The Rust core runs in-process (loaded as a cdylib via the generated UniFFI
 * bindings). We boot the node on a background coroutine, then point GeckoView at
 * the local node URL. `.epix` navigation and the EpixNet #15 CSP/clearnet policy
 * are enforced by the engine (server-side CSP) plus a bundled WebExtension
 * (installBuiltIn + webRequest) - the GeckoView way, since it has no
 * shouldInterceptRequest. That extension is a follow-up.
 *
 * The surface looks like a browser: an address bar (type `talk.epix`, an
 * `epix1…` address, or any URL) and, Brave-style, the Epix icon next to it.
 * The icon wears the Tor state as a badge (the desktop extension's colors:
 * gray off, amber connecting, purple ready, green when all traffic routes
 * through Tor) and opens the Epix panel - current xite, Tor status, our onion
 * address - when tapped.
 */
class MainActivity : AppCompatActivity() {

    private lateinit var geckoView: GeckoView
    private lateinit var session: GeckoSession
    private lateinit var addressBar: EditText
    private lateinit var torBadge: GradientDrawable
    private lateinit var runtime: GeckoRuntime
    private val node = EpixNode()
    private val scope = CoroutineScope(Dispatchers.Main)
    private var canGoBack = false
    private var currentDisplay: String = ""
    /// Route clearnet browsing through the node's Tor SOCKS listener. Default
    /// on (opt-out), like the desktop extension. Persisted across launches.
    private var torClearnet = true
    /// The embedded Epix Wallet (the desktop browser's extension, installed
    /// built-in); tapping the Epix button opens its popup document.
    private var wallet: WebExtension? = null
    private var walletDialog: AlertDialog? = null

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

        torClearnet = getPreferences(Context.MODE_PRIVATE).getBoolean(PREF_TOR_CLEARNET, true)

        setContentView(buildBrowserChrome())

        runtime = GeckoRuntime.getDefault(this)
        installWallet()
        session = GeckoSession().apply { open(runtime) }
        session.navigationDelegate = object : GeckoSession.NavigationDelegate {
            override fun onLocationChange(
                session: GeckoSession,
                url: String?,
                perms: MutableList<GeckoSession.PermissionDelegate.ContentPermission>,
                hasUserGesture: Boolean,
            ) {
                if (url != null && !addressBar.hasFocus()) {
                    addressBar.setText(friendlyUrl(url))
                }
            }

            override fun onCanGoBack(session: GeckoSession, value: Boolean) {
                canGoBack = value
            }
        }
        geckoView.setSession(session)
        // The page, not the address bar, starts focused - otherwise the
        // keyboard pops over the app on every launch.
        geckoView.requestFocus()

        // The xite to open: from an epix:// launch intent, else the dashboard.
        val target = intentTarget(intent) ?: "dashboard.epix"

        scope.launch {
            val display = bootNode(target)
            if (display != null) {
                currentDisplay = display
                // Apply the saved clearnet-through-Tor routing before the first
                // page load, so clearnet requests are proxied from the start.
                applyClearnetRouting()
                session.loadUri(nodeUrl(display))
            } else {
                session.loadUri("about:blank") // TODO: show a boot-error page
            }
        }

        // Reflect the Tor state in the icon's badge, at the extension's cadence.
        scope.launch {
            while (true) {
                torStatus()?.let { torBadge.setColor(colorFor(it)) }
                delay(5_000)
            }
        }
    }

    /** Hardware/gesture back navigates the page history first. */
    @Deprecated("Deprecated in Java")
    override fun onBackPressed() {
        if (canGoBack) session.goBack() else @Suppress("DEPRECATION") super.onBackPressed()
    }

    /** A second epix:// link while running: navigate the existing session. */
    override fun onNewIntent(intent: Intent) {
        super.onNewIntent(intent)
        val target = intentTarget(intent) ?: return
        currentDisplay = target
        session.loadUri(nodeUrl(target))
    }

    /** The browser chrome: address bar + Epix icon on top, the page below. */
    private fun buildBrowserChrome(): View {
        val root = LinearLayout(this).apply {
            orientation = LinearLayout.VERTICAL
            fitsSystemWindows = true
            setBackgroundColor(COLOR_CHROME_BG)
        }

        val bar = LinearLayout(this).apply {
            orientation = LinearLayout.HORIZONTAL
            gravity = Gravity.CENTER_VERTICAL
            setPadding(dp(8), dp(6), dp(4), dp(6))
        }

        addressBar = EditText(this).apply {
            hint = "Type a .epix name or address"
            setHintTextColor(COLOR_HINT)
            setTextColor(COLOR_TEXT)
            textSize = 14f
            isSingleLine = true
            inputType = android.text.InputType.TYPE_TEXT_VARIATION_URI
            imeOptions = EditorInfo.IME_ACTION_GO
            setPadding(dp(14), dp(8), dp(14), dp(8))
            background = GradientDrawable().apply {
                cornerRadius = dp(18).toFloat()
                setColor(COLOR_FIELD_BG)
            }
            setSelectAllOnFocus(true)
            setOnEditorActionListener { _, actionId, _ ->
                if (actionId == EditorInfo.IME_ACTION_GO) {
                    navigate(text.toString())
                    true
                } else {
                    false
                }
            }
        }
        bar.addView(addressBar, LinearLayout.LayoutParams(0, -2, 1f))

        // The Epix button (Brave-style): the logo with the Tor state as a
        // badge; tapping opens the Epix panel.
        torBadge = GradientDrawable().apply {
            shape = GradientDrawable.OVAL
            setColor(COLOR_OFF)
            setStroke(dp(2), COLOR_CHROME_BG)
        }
        val icon = android.widget.ImageView(this).apply {
            setImageResource(R.drawable.epix_icon)
            scaleType = android.widget.ImageView.ScaleType.FIT_CENTER
            adjustViewBounds = true
        }
        val badge = View(this).apply { background = torBadge }
        val holder = FrameLayout(this).apply {
            contentDescription = "Epix wallet"
            addView(icon, FrameLayout.LayoutParams(dp(36), dp(36), Gravity.CENTER))
            addView(
                badge,
                FrameLayout.LayoutParams(dp(13), dp(13), Gravity.BOTTOM or Gravity.END).apply {
                    bottomMargin = dp(3)
                    marginEnd = dp(3)
                },
            )
            // Tap opens the wallet (Brave-style); its shield carries the same
            // Tor/I2P panel. Long-press (or no wallet) opens the plain panel.
            setOnClickListener {
                wallet?.let { showWalletPopup(it) } ?: showEpixPanel()
            }
            setOnLongClickListener {
                showEpixPanel()
                true
            }
        }
        bar.addView(holder, LinearLayout.LayoutParams(dp(44), dp(44)))

        root.addView(bar, LinearLayout.LayoutParams(-1, -2))
        geckoView = GeckoView(this)
        root.addView(geckoView, LinearLayout.LayoutParams(-1, 0, 1f))
        return root
    }

    /** Turn what the user typed into somewhere to go. */
    private fun navigate(input: String) {
        val t = input.trim()
        if (t.isEmpty()) return
        val url = when {
            t.startsWith("http://") || t.startsWith("https://") -> t
            t.startsWith("epix://") -> {
                val host = t.removePrefix("epix://").substringBefore('/')
                currentDisplay = host
                nodeUrl(host)
            }
            t.startsWith("epix1") || t.endsWith(".epix") -> {
                currentDisplay = t
                nodeUrl(t)
            }
            // Looks like a clearnet domain: browse it over https.
            t.contains('.') && !t.contains(' ') -> "https://$t"
            // A bare word is a .epix name.
            else -> {
                currentDisplay = "$t.epix"
                nodeUrl("$t.epix")
            }
        }
        session.loadUri(url)
        addressBar.clearFocus()
        (getSystemService(Context.INPUT_METHOD_SERVICE) as InputMethodManager)
            .hideSoftInputFromWindow(addressBar.windowToken, 0)
    }

    /** Show `talk.epix/…` in the bar, not the local node plumbing. */
    private fun friendlyUrl(url: String): String {
        val prefix = "$NODE_BASE/"
        if (!url.startsWith(prefix)) return url
        val rest = url.removePrefix(prefix).trimEnd('/')
        rest.substringBefore('/').takeIf { it.isNotEmpty() }?.let { currentDisplay = it }
        return rest
    }

    /** Boot the node off the main thread; return the display name to open. */
    private suspend fun bootNode(target: String): String? = withContext(Dispatchers.IO) {
        try {
            val config = NodeConfig(
                dataDir = filesDir.absolutePath,
                target = target,
                uiAddr = "127.0.0.1:$UI_PORT",
                torMode = "enable",
                version = "0.1.0",
            )
            node.start(config)
            if (node.state() == NodeState.SERVING) target else null
        } catch (e: Exception) {
            null
        }
    }

    /** The node's Tor state, fetched off the main thread (the call blocks). */
    private suspend fun torStatus(): TorStatus? = withContext(Dispatchers.IO) {
        runCatching { node.torStatus() }.getOrNull()
    }

    /**
     * Same color language as the desktop extension's toolbar icon: green when
     * Tor is on AND clearnet is routed through it, purple when Tor is ready but
     * clearnet goes direct, amber connecting, gray off.
     */
    private fun colorFor(st: TorStatus): Int = when {
        st.enabled -> if (torClearnet) COLOR_ROUTED else COLOR_READY
        st.status == "Bootstrapping" -> COLOR_BOOT
        else -> COLOR_OFF
    }

    /**
     * The Epix panel: current xite, Tor status, our onion address, and the
     * "route clearnet through Tor" switch (the desktop extension's popup).
     */
    private fun showEpixPanel() {
        scope.launch {
            val st = torStatus()
            val onion = withContext(Dispatchers.IO) {
                runCatching { node.onionAddress() }.getOrNull()
            }
            val torLine = when {
                st == null -> "Tor: unknown"
                st.enabled && torClearnet -> "Tor: on - clearnet routed through Tor"
                st.enabled -> "Tor: ready - onion peers reachable"
                st.status == "Bootstrapping" -> "Tor: connecting…"
                st.status == "Failed" -> "Tor: failed to start"
                else -> "Tor: off"
            }

            val pad = dp(20)
            val layout = LinearLayout(this@MainActivity).apply {
                orientation = LinearLayout.VERTICAL
                setPadding(pad, dp(12), pad, 0)
            }
            if (currentDisplay.isNotEmpty()) {
                layout.addView(label("Xite: $currentDisplay"))
            }
            layout.addView(label(torLine))
            if (onion != null) {
                layout.addView(label("\nYour onion address:\n$onion.onion"))
            }
            val row = LinearLayout(this@MainActivity).apply {
                orientation = LinearLayout.HORIZONTAL
                gravity = Gravity.CENTER_VERTICAL
                setPadding(0, dp(14), 0, 0)
            }
            row.addView(
                label("Route clearnet through Tor"),
                LinearLayout.LayoutParams(0, -2, 1f),
            )
            val toggle = Switch(this@MainActivity).apply {
                isChecked = torClearnet
                setOnCheckedChangeListener { _, checked -> setTorClearnet(checked) }
            }
            row.addView(toggle)
            layout.addView(row)

            AlertDialog.Builder(this@MainActivity)
                .setTitle("Epix")
                .setView(layout)
                .setPositiveButton("OK", null)
                .show()
        }
    }

    /**
     * Install the bundled Epix Wallet WebExtension (the same forked-Keplr
     * build the desktop browser embeds, staged into assets by gradle's
     * `stageWalletExt`) and wire up its two host hooks:
     *
     * - Native messaging: the wallet's background bridge sends the desktop
     *   `zone.epix.nmh` commands; here the app itself is the host, answering
     *   from the in-process node (status over loopback, the clearnet toggle
     *   and per-site allowances from this activity's state).
     * - The toolbar action: tapping the Epix button clicks it, and the popup
     *   session it requests is shown in a dialog over the browser.
     */
    private fun installWallet() {
        runtime.webExtensionController
            .ensureBuiltIn("resource://android/assets/extensions/wallet/", WALLET_EXT_ID)
            .accept({ ext ->
                if (ext == null) return@accept
                wallet = ext
                ext.setMessageDelegate(nmhDelegate, NMH_NAME)
                ext.setActionDelegate(walletActionDelegate)
                // The wallet opens full pages (register, sign approvals) with
                // tabs.create; render those as near-full-screen sheets. The
                // app loads the URL itself (GeckoView hands it over raw, and
                // extension-relative paths like "/register.html" need the
                // moz-extension base put back).
                ext.tabDelegate = object : WebExtension.TabDelegate {
                    override fun onNewTab(
                        source: WebExtension,
                        details: WebExtension.CreateTabDetails,
                    ): GeckoResult<GeckoSession> {
                        val session = openPopupSession(heightFraction = 0.95)
                        val url = details.url ?: ""
                        val resolved = when {
                            url.isEmpty() -> null
                            url.contains("://") -> url
                            else ->
                                source.metaData.baseUrl.trimEnd('/') +
                                    (if (url.startsWith("/")) url else "/$url")
                        }
                        resolved?.let { session.loadUri(it) }
                        return GeckoResult.fromValue(session)
                    }
                }
            }, { e ->
                android.util.Log.w("EpixWallet", "wallet install failed", e)
            })
    }

    /** The app-side native-messaging host: the desktop `epix-nmh` commands. */
    private val nmhDelegate = object : WebExtension.MessageDelegate {
        override fun onMessage(
            nativeApp: String,
            message: Any,
            sender: WebExtension.MessageSender,
        ): GeckoResult<Any>? {
            val req = message as? JSONObject ?: return null
            val result = GeckoResult<Any>()
            // The response rides Gecko's EventDispatcher, which only accepts
            // its own bundle type, not JSONObject ("Invalid event data").
            scope.launch {
                result.complete(GeckoBundle.fromJSONObject(handleNmh(req)))
            }
            return result
        }
    }

    /**
     * GeckoView's message bridge rejects JSON null values ("Invalid event
     * data"); the node's status JSON uses them for absent addresses. Absent
     * key and null mean the same to the wallet, so drop them.
     */
    private fun stripNulls(o: JSONObject): JSONObject {
        val out = JSONObject()
        for (key in o.keys()) {
            val v = o.get(key)
            if (v == JSONObject.NULL) continue
            out.put(key, if (v is JSONObject) stripNulls(v) else v)
        }
        return out
    }

    /** Answer one native-host command with the same JSON shapes as epix-nmh. */
    private suspend fun handleNmh(req: JSONObject): JSONObject =
        stripNulls(handleNmhRaw(req))

    private suspend fun handleNmhRaw(req: JSONObject): JSONObject {
        return when (req.optString("cmd")) {
            "status" -> withContext(Dispatchers.IO) {
                try {
                    val text = java.net.URL("$NODE_BASE/EpixNet-Internal/Status")
                        .openConnection()
                        .apply { connectTimeout = 2_000; readTimeout = 2_000 }
                        .getInputStream()
                        .bufferedReader()
                        .readText()
                    JSONObject(text)
                } catch (e: Exception) {
                    JSONObject().put("serving", false)
                }
            }.put("ui_port", UI_PORT).put("tor_clearnet", torClearnet)
            "getTorClearnet" -> JSONObject().put("on", torClearnet)
            "setTorClearnet" -> {
                val on = req.optBoolean("on", false)
                setTorClearnet(on)
                JSONObject().put("ok", true).put("on", on)
            }
            "getClearnetAllow" ->
                JSONObject().put("allow", allowedSites().contains(req.optString("site")))
            "setClearnetAllow" -> {
                val site = req.optString("site")
                val sites = allowedSites().toMutableSet()
                if (req.optBoolean("allow")) sites.add(site) else sites.remove(site)
                getPreferences(Context.MODE_PRIVATE).edit()
                    .putStringSet(PREF_CLEARNET_ALLOW, sites).apply()
                JSONObject().put("ok", true)
            }
            "listClearnetAllow" ->
                JSONObject().put("sites", JSONArray(allowedSites().toList()))
            "openConfig" -> {
                // Close the wallet sheet and point the browser at the node's
                // config page (the dashboard's Config lives at the UI origin).
                withContext(Dispatchers.Main) {
                    walletDialog?.dismiss()
                    currentDisplay = "Config"
                    session.loadUri("$NODE_BASE/Config")
                }
                JSONObject().put("ok", true)
            }
            else -> JSONObject().put("error", "unknown command: ${req.optString("cmd")}")
        }
    }

    /** Sites the user allowed to reach clearnet from a `.epix` page. */
    private fun allowedSites(): Set<String> =
        getPreferences(Context.MODE_PRIVATE).getStringSet(PREF_CLEARNET_ALLOW, emptySet())!!

    /**
     * The extension side of popups: if the wallet itself asks to open or
     * toggle its popup, give it a session rendered in the same dialog. (The
     * Epix button does not go through the Action - it loads the popup
     * document directly, see [showWalletPopup].)
     */
    private val walletActionDelegate = object : WebExtension.ActionDelegate {
        override fun onTogglePopup(
            extension: WebExtension,
            action: WebExtension.Action,
        ): GeckoResult<GeckoSession>? {
            walletDialog?.let {
                it.dismiss()
                return null
            }
            return GeckoResult.fromValue(openPopupSession())
        }

        override fun onOpenPopup(
            extension: WebExtension,
            action: WebExtension.Action,
        ): GeckoResult<GeckoSession>? = GeckoResult.fromValue(openPopupSession())
    }

    /** Open the wallet UI: its popup document in a dialog over the browser. */
    private fun showWalletPopup(ext: WebExtension) {
        walletDialog?.let {
            it.dismiss()
            return
        }
        val base = ext.metaData.baseUrl
        openPopupSession().loadUri("${base}popup.html")
    }

    /** A popup-sized dialog holding a fresh GeckoSession for the wallet UI. */
    private fun openPopupSession(heightFraction: Double = 0.82): GeckoSession {
        // One wallet surface at a time: a tab opened from the popup (register,
        // sign approval) replaces the popup's dialog.
        walletDialog?.dismiss()
        val popupSession = GeckoSession().apply { open(runtime) }
        val view = GeckoView(this).apply { setSession(popupSession) }
        // A slim header with an explicit close control: the sheet covers the
        // browser, and the back gesture / tap-outside affordances are not
        // discoverable on their own.
        val closeButton = TextView(this).apply {
            text = "✕"
            textSize = 16f
            setTextColor(COLOR_TEXT)
            gravity = Gravity.CENTER
            contentDescription = "Close wallet"
            setPadding(dp(14), dp(6), dp(14), dp(6))
            setOnClickListener { walletDialog?.dismiss() }
        }
        val header = LinearLayout(this).apply {
            orientation = LinearLayout.HORIZONTAL
            gravity = Gravity.CENTER_VERTICAL
            setBackgroundColor(COLOR_CHROME_BG)
            addView(
                TextView(this@MainActivity).apply {
                    text = "Epix Wallet"
                    textSize = 14f
                    setTextColor(COLOR_TEXT)
                    setPadding(dp(16), dp(6), 0, dp(6))
                },
                LinearLayout.LayoutParams(0, -2, 1f),
            )
            addView(closeButton)
        }
        // The wallet popup lays out at the extension-popup size; give it most
        // of the screen (minus the header), dashboard-dark behind it.
        val height = (resources.displayMetrics.heightPixels * heightFraction).toInt() - dp(40)
        val holder = LinearLayout(this).apply {
            orientation = LinearLayout.VERTICAL
            setBackgroundColor(COLOR_CHROME_BG)
            addView(header, LinearLayout.LayoutParams(-1, -2))
            addView(view, LinearLayout.LayoutParams(-1, height))
        }
        popupSession.contentDelegate = object : GeckoSession.ContentDelegate {
            override fun onCloseRequest(session: GeckoSession) {
                walletDialog?.dismiss()
            }
        }
        // The wallet closes its own pages with tabs.remove (e.g. after
        // registration); treat that like a close request.
        wallet?.let { ext ->
            popupSession.webExtensionController.setTabDelegate(
                ext,
                object : WebExtension.SessionTabDelegate {
                    override fun onCloseTab(
                        source: WebExtension?,
                        session: GeckoSession,
                    ): GeckoResult<org.mozilla.geckoview.AllowOrDeny> {
                        walletDialog?.dismiss()
                        return GeckoResult.allow()
                    }
                },
            )
        }
        val dialog = AlertDialog.Builder(this)
            .setView(holder)
            .create()
        dialog.setOnDismissListener {
            popupSession.close()
            // Only clear the field if it still points at this dialog: when one
            // popup replaces another (popup -> register tab), dismiss() runs
            // its listener asynchronously, so this can fire after the
            // replacement dialog was already stored - clearing it then would
            // drop the reference to the visible dialog and break its close
            // button.
            if (walletDialog === dialog) {
                walletDialog = null
            }
        }
        walletDialog = dialog
        dialog.show()
        // Full width: the wallet pages are laid out for a browser surface, not
        // a margined dialog.
        dialog.window?.setLayout(-1, -2)
        return popupSession
    }

    private fun label(text: String): TextView =
        TextView(this).apply {
            this.text = text
            textSize = 14f
            setPadding(0, dp(4), 0, dp(4))
        }

    /** Flip the clearnet-through-Tor routing, persist it, and apply it live. */
    private fun setTorClearnet(on: Boolean) {
        if (torClearnet == on) return
        torClearnet = on
        getPreferences(Context.MODE_PRIVATE).edit().putBoolean(PREF_TOR_CLEARNET, on).apply()
        applyClearnetRouting()
        // The badge reflects the new routing state at once, not on the next poll.
        torBadge.setColor(if (on) COLOR_ROUTED else COLOR_READY)
    }

    /**
     * Point GeckoView's proxy at the node's Tor SOCKS listener (or direct).
     * When on, all non-loopback (clearnet) requests go through Tor; the node's
     * own loopback - the UI and every `.epix` page served from 127.0.0.1 - is
     * never proxied (allow_hijacking_localhost stays false), so xites keep
     * loading directly. socks_remote_dns keeps DNS inside Tor. This is the
     * runtime equivalent of the desktop launcher's file PAC.
     */
    private fun applyClearnetRouting() {
        val user = GeckoPreferenceController.PREF_BRANCH_USER
        if (torClearnet) {
            GeckoPreferenceController.setGeckoPref("network.proxy.type", 1, user)
            GeckoPreferenceController.setGeckoPref("network.proxy.socks", "127.0.0.1", user)
            GeckoPreferenceController.setGeckoPref("network.proxy.socks_port", SOCKS_PORT, user)
            GeckoPreferenceController.setGeckoPref("network.proxy.socks_version", 5, user)
            GeckoPreferenceController.setGeckoPref("network.proxy.socks_remote_dns", true, user)
            GeckoPreferenceController.setGeckoPref(
                "network.proxy.allow_hijacking_localhost", false, user,
            )
        } else {
            GeckoPreferenceController.setGeckoPref("network.proxy.type", 0, user)
        }
    }

    private fun nodeUrl(name: String): String = "$NODE_BASE/$name/"

    private fun dp(v: Int): Int = (v * resources.displayMetrics.density).toInt()

    /** Pull the xite target out of an `epix://host/path` VIEW intent. */
    private fun intentTarget(intent: Intent?): String? {
        val data: Uri = intent?.data ?: return null
        if (data.scheme != "epix") return null
        return data.host ?: data.toString().removePrefix("epix://").substringBefore('/')
    }

    companion object {
        init {
            // The Rust core (libepix_ffi.so, one per ABI) packaged in jniLibs.
            System.loadLibrary("epix_ffi")
        }

        // The node's local Tor SOCKS listener (epix-node boot: 43111).
        private const val SOCKS_PORT = 43111
        private const val PREF_TOR_CLEARNET = "torClearnet"
        private const val PREF_CLEARNET_ALLOW = "clearnetAllow"

        // The embedded node's UI server (bootNode's uiAddr): the EpixNet
        // default port. The app boots the node itself, so unlike the desktop
        // native host there is no discovery/legacy-port fallback to do here.
        private const val UI_PORT = 42222
        private const val NODE_BASE = "http://127.0.0.1:$UI_PORT"

        // Must match the wallet manifest's gecko id and its native host name
        // (the desktop crates/epix-browser/src/ext.rs values).
        private const val WALLET_EXT_ID = "wallet@epix.zone"
