    private var nodePageRequested = false

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

        torClearnet = getPreferences(Context.MODE_PRIVATE).getBoolean(PREF_TOR_CLEARNET, true)

        val container = FrameLayout(this)
        container.addView(
            buildBrowserChrome(),
            FrameLayout.LayoutParams(-1, -1),
        )
        splashOverlay = buildSplash().also {
            container.addView(it, FrameLayout.LayoutParams(-1, -1))
        }
        setContentView(container)

        runtime = GeckoRuntime.getDefault(this)
        installWallet()
        session = GeckoSession().apply { open(runtime) }
        session.navigationDelegate = object : GeckoSession.NavigationDelegate {
            override fun onLocationChange(
                session: GeckoSession,
                url: String?,
                perms: MutableList<GeckoSession.PermissionDelegate.ContentPermission>,
                hasUserGesture: Boolean,
            ) {
                if (url != null && !addressBar.hasFocus()) {
                    addressBar.setText(friendlyUrl(url))
                }
            }

            override fun onCanGoBack(session: GeckoSession, value: Boolean) {
                canGoBack = value
            }

            override fun onLoadError(
                session: GeckoSession,
                uri: String?,
                error: WebRequestError,
            ): GeckoResult<String>? {
                // GeckoView renders nothing on a load error unless the app
                // supplies an error page, so a refused connection to the node
                // (its UI still coming up) used to read as a blank app. Wait
                // for the port and retry; only if the node stays unreachable
                // show a real error page.
                if (uri == null || !uri.startsWith(NODE_BASE)) return null
                if (nodeLoadRetries < MAX_NODE_LOAD_RETRIES) {
                    nodeLoadRetries++
                    scope.launch {
                        awaitUiPort()
                        session.loadUri(uri)
                    }
                    return null
                }
                return GeckoResult.fromValue(errorPage())
            }
        }
        session.progressDelegate = object : GeckoSession.ProgressDelegate {
            override fun onPageStop(session: GeckoSession, success: Boolean) {
                // A page made it through: later transient errors get a fresh
                // retry budget.
                if (success) nodeLoadRetries = 0
                // The first real page (dashboard or the error page) is up, so
                // the boot/connect wait is over - drop the loading splash. Skip
                // the session's initial about:blank stop (nodePageRequested).
                if (nodePageRequested) hideSplash()
            }
        }
        // GeckoView draws no UI of its own for content prompts: without a
        // PromptDelegate, <select> dropdowns and JS alert/confirm/prompt taps
        // silently do nothing (the Config page's dropdowns and reset
        // confirms). WKWebView handles these natively on iOS; this is the
        // Android equivalent, backed by plain AlertDialogs.
        session.promptDelegate = buildPromptDelegate()
        geckoView.setSession(session)
        // The page, not the address bar, starts focused - otherwise the
        // keyboard pops over the app on every launch.
        geckoView.requestFocus()

        // The xite to open: from an epix:// launch intent, else the dashboard.
        val target = intentTarget(intent) ?: "dashboard.epix"

        scope.launch {
            // Apply the saved clearnet-through-Tor routing before the first page
            // load, so clearnet requests are proxied from the start. Loopback
            // (the dashboard and every .epix page) is exempt, so this is safe to
            // set before the node is up.
            applyClearnetRouting()
            val display = bootNode(target)
            // Load the dashboard even if boot reported a problem: the node
            // serves its own loading/error wrapper on loopback, which is more
            // useful than a blank page and lets the user retry.
            currentDisplay = display ?: target
            // Don't hand GeckoView the URL until loopback actually accepts
            // (the desktop launcher's wait_for_port): a refused first
            // connection renders as a silent blank page. The node now binds
            // before start() returns, so this passes immediately - it guards
            // the boot-failed path and anything else that delays the bind.
            // From here the next page-stop is a real page, so let it drop the
            // splash (about:blank already came and went).
            nodePageRequested = true
            if (awaitUiPort()) {
                session.loadUri(nodeUrl(currentDisplay))
            } else {
                session.loadUri(errorPage())
            }
        }

        // Reflect the Tor state in the icon's badge, at the extension's cadence.
        scope.launch {
            while (true) {
                torStatus()?.let { torBadge.setColor(colorFor(it)) }
                delay(5_000)
            }
        }
    }

    /** Content prompt UI (selects, alert/confirm/prompt) over AlertDialogs. */
    private fun buildPromptDelegate() = object : GeckoSession.PromptDelegate {
        /// A prompt must be answered exactly once, but a dialog's dismiss
        /// listener also runs after a button click. The response is built
        /// lazily INSIDE the guard: even `prompt.dismiss()` throws
        /// "Cannot confirm/dismiss a Prompt twice" once the prompt is
        /// complete, so it must not be evaluated as a call argument (that
        /// exact pattern crashed the app on every confirmed dialog, e.g.
        /// deleting a backup).
        fun done(
            res: GeckoResult<GeckoSession.PromptDelegate.PromptResponse>,
            prompt: GeckoSession.PromptDelegate.BasePrompt,
            respond: () -> GeckoSession.PromptDelegate.PromptResponse,
        ) {
            if (prompt.isComplete) return
            runCatching { res.complete(respond()) }
        }

        override fun onAlertPrompt(
            session: GeckoSession,
            prompt: GeckoSession.PromptDelegate.AlertPrompt,
        ): GeckoResult<GeckoSession.PromptDelegate.PromptResponse> {
            val res = GeckoResult<GeckoSession.PromptDelegate.PromptResponse>()
            val dialog = AlertDialog.Builder(this@MainActivity)
                .setTitle(prompt.title)
                .setMessage(prompt.message)
                .setPositiveButton(android.R.string.ok, null)
                .create()
            dialog.setOnDismissListener { done(res, prompt) { prompt.dismiss() } }
            dialog.show()
            return res
        }

        override fun onButtonPrompt(
            session: GeckoSession,
            prompt: GeckoSession.PromptDelegate.ButtonPrompt,
        ): GeckoResult<GeckoSession.PromptDelegate.PromptResponse> {
            val res = GeckoResult<GeckoSession.PromptDelegate.PromptResponse>()
            val dialog = AlertDialog.Builder(this@MainActivity)
                .setTitle(prompt.title)
                .setMessage(prompt.message)
                .setPositiveButton(android.R.string.ok) { _, _ ->
                    done(res, prompt) {
                        prompt.confirm(GeckoSession.PromptDelegate.ButtonPrompt.Type.POSITIVE)
                    }
                }
                .setNegativeButton(android.R.string.cancel) { _, _ ->
                    done(res, prompt) {
                        prompt.confirm(GeckoSession.PromptDelegate.ButtonPrompt.Type.NEGATIVE)
                    }
                }
                .create()
            dialog.setOnDismissListener { done(res, prompt) { prompt.dismiss() } }
            dialog.show()
            return res
        }

        override fun onTextPrompt(
            session: GeckoSession,
            prompt: GeckoSession.PromptDelegate.TextPrompt,
        ): GeckoResult<GeckoSession.PromptDelegate.PromptResponse> {
            val res = GeckoResult<GeckoSession.PromptDelegate.PromptResponse>()
            val input = EditText(this@MainActivity).apply { setText(prompt.defaultValue ?: "") }
            val pad = (16 * resources.displayMetrics.density).toInt()
            val wrap = FrameLayout(this@MainActivity).apply {
                setPadding(pad, 0, pad, 0)
                addView(input)
            }
            val dialog = AlertDialog.Builder(this@MainActivity)
                .setTitle(prompt.title)
                .setMessage(prompt.message)
                .setView(wrap)
                .setPositiveButton(android.R.string.ok) { _, _ ->
                    done(res, prompt) { prompt.confirm(input.text.toString()) }
                }
                .setNegativeButton(android.R.string.cancel, null)
                .create()
            dialog.setOnDismissListener { done(res, prompt) { prompt.dismiss() } }
            dialog.show()
            return res
        }

        override fun onChoicePrompt(
            session: GeckoSession,
            prompt: GeckoSession.PromptDelegate.ChoicePrompt,
        ): GeckoResult<GeckoSession.PromptDelegate.PromptResponse> {
            val res = GeckoResult<GeckoSession.PromptDelegate.PromptResponse>()
            // Flatten one optgroup level; our pages only use flat selects.
            val flat = mutableListOf<GeckoSession.PromptDelegate.ChoicePrompt.Choice>()
            for (c in prompt.choices) {
                val subs = c.items
                if (subs.isNullOrEmpty()) flat.add(c) else flat.addAll(subs)
            }
            val labels = flat.map { it.label }.toTypedArray()
            val builder = AlertDialog.Builder(this@MainActivity).setTitle(prompt.title)
            if (prompt.type == GeckoSession.PromptDelegate.ChoicePrompt.Type.MULTIPLE) {
                val checked = BooleanArray(flat.size) { flat[it].selected }
                builder.setMultiChoiceItems(labels, checked) { _, which, isChecked ->
                    checked[which] = isChecked
                }
                builder.setPositiveButton(android.R.string.ok) { _, _ ->
                    val ids = flat.filterIndexed { i, _ -> checked[i] }.map { it.id }.toTypedArray()
                    done(res, prompt) { prompt.confirm(ids) }
                }
                builder.setNegativeButton(android.R.string.cancel, null)
            } else {
                val selected = flat.indexOfFirst { it.selected }
                builder.setSingleChoiceItems(labels, selected) { dialog, which ->
                    done(res, prompt) { prompt.confirm(flat[which]) }
                    dialog.dismiss()
                }
            }
            val dialog = builder.create()
            dialog.setOnDismissListener { done(res, prompt) { prompt.dismiss() } }
            dialog.show()
            return res
        }

        override fun onFilePrompt(
            session: GeckoSession,
            prompt: GeckoSession.PromptDelegate.FilePrompt,
        ): GeckoResult<GeckoSession.PromptDelegate.PromptResponse> {
            // GeckoView draws no file picker either: without this, tapping a
            // page's file input (e.g. Backup & Restore's upload) does nothing.
            // Hand off to the system document picker; the result comes back in
            // onActivityResult.
            val res = GeckoResult<GeckoSession.PromptDelegate.PromptResponse>()
            pendingFilePrompt = prompt
            pendingFileResult = res
            val intent = Intent(Intent.ACTION_GET_CONTENT).apply {
                addCategory(Intent.CATEGORY_OPENABLE)
                // No mime filter: downloaded backups often carry a generic
                // type, and a strict filter would gray them out. The node
                // validates the file server-side anyway.
                type = "*/*"
                if (prompt.type == GeckoSession.PromptDelegate.FilePrompt.Type.MULTIPLE) {
                    putExtra(Intent.EXTRA_ALLOW_MULTIPLE, true)
                }
            }
            @Suppress("DEPRECATION")
            startActivityForResult(intent, FILE_PICK_REQUEST)
            return res
        }
    }

    /** The system file picker's answer for a parked onFilePrompt. */
    @Deprecated("Deprecated in Java")
    override fun onActivityResult(requestCode: Int, resultCode: Int, data: Intent?) {
        @Suppress("DEPRECATION")
        super.onActivityResult(requestCode, resultCode, data)
        if (requestCode != FILE_PICK_REQUEST) return
        val prompt = pendingFilePrompt ?: return
        val res = pendingFileResult ?: return
        pendingFilePrompt = null
        pendingFileResult = null
        if (prompt.isComplete) return
        // A multi-select answer arrives as clipData; a single pick as data.
        val uris = mutableListOf<Uri>()
        data?.clipData?.let { clip -> for (i in 0 until clip.itemCount) uris.add(clip.getItemAt(i).uri) }
        data?.data?.takeIf { uris.isEmpty() }?.let { uris.add(it) }
        if (resultCode != RESULT_OK || uris.isEmpty()) {
            runCatching { res.complete(prompt.dismiss()) }
            return
        }
        // Gecko cannot open a picker's content:// grant (its file delegate
        // fails with NS_ERROR_FILE_UNRECOGNIZED_PATH and the input stays
        // empty), so copy each pick into the app cache and hand Gecko real
        // file:// URIs. Copied off the main thread - a backup can be huge.
        scope.launch {
            val files = withContext(Dispatchers.IO) {
                val dir = java.io.File(cacheDir, "picked")
                dir.deleteRecursively()
                dir.mkdirs()
                uris.mapNotNull { uri -> copyPickedFile(uri, dir) }
            }
            runCatching {
                when {
                    files.isEmpty() -> res.complete(prompt.dismiss())
                    files.size == 1 ->
                        res.complete(prompt.confirm(this@MainActivity, Uri.fromFile(files[0])))
                    else -> res.complete(
                        prompt.confirm(
                            this@MainActivity,
                            files.map { Uri.fromFile(it) }.toTypedArray(),
                        ),
                    )
                }
            }
        }
    }

    /** Copy one picked content:// item into `dir`, keeping its display name. */
    private fun copyPickedFile(uri: Uri, dir: java.io.File): java.io.File? = runCatching {
        val name = (queryDisplayName(uri) ?: "upload.bin").replace('/', '_')
        val out = java.io.File(dir, name)
        contentResolver.openInputStream(uri)!!.use { input ->
            out.outputStream().use { input.copyTo(it) }
        }
        out
    }.getOrNull()

    /** The user-facing file name behind a content:// Uri, if the provider says. */
    private fun queryDisplayName(uri: Uri): String? = runCatching {
        contentResolver.query(uri, null, null, null, null)?.use { c ->
            val i = c.getColumnIndex(android.provider.OpenableColumns.DISPLAY_NAME)
            if (c.moveToFirst() && i >= 0) c.getString(i) else null
        }
    }.getOrNull()

