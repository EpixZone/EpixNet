            setColor(COLOR_OFF)
            setStroke(dp(2), COLOR_CHROME_BG)
        }
        val icon = android.widget.ImageView(this).apply {
            // The white Epix mark on a dark disc (the disc is the chrome color,
            // so it reads as a clean white mark) - matches the desktop browser's
            // reskinned toolbar icon (EpixNet PR #231).
            setImageResource(R.drawable.epix_badge_white)
            scaleType = android.widget.ImageView.ScaleType.FIT_CENTER
            adjustViewBounds = true
        }
        val badge = View(this).apply { background = torBadge }
        val holder = FrameLayout(this).apply {
            contentDescription = "Epix wallet"
            addView(icon, FrameLayout.LayoutParams(dp(36), dp(36), Gravity.CENTER))
            addView(
                badge,
                FrameLayout.LayoutParams(dp(13), dp(13), Gravity.BOTTOM or Gravity.END).apply {
                    bottomMargin = dp(3)
                    marginEnd = dp(3)
                },
            )
            // Tap opens the wallet (Brave-style); its shield carries the same
            // Tor/I2P panel. Long-press (or no wallet) opens the plain panel.
            setOnClickListener {
                wallet?.let { showWalletPopup(it) } ?: showEpixPanel()
            }
            setOnLongClickListener {
                showEpixPanel()
                true
            }
        }
        bar.addView(holder, LinearLayout.LayoutParams(dp(44), dp(44)))

        root.addView(bar, LinearLayout.LayoutParams(-1, -2))
        geckoView = GeckoView(this)
        // Dark until the page's first paint; GeckoView otherwise flashes
        // white while the node boots and the page loads.
        geckoView.coverUntilFirstPaint(COLOR_CHROME_BG)
        root.addView(geckoView, LinearLayout.LayoutParams(-1, 0, 1f))
        return root
    }

    /**
     * The loading splash: the white Epix mark spinning on the dark chrome
     * background, shown from launch until the first page paints. Mirrors the
     * desktop browser's spinning toolbar icon (EpixNet PR #231) - on a cold
     * start the node bootstraps Tor for tens of seconds, so this covers that
     * wait instead of a blank dark screen.
     */
    private fun buildSplash(): View {
        val overlay = FrameLayout(this).apply {
            setBackgroundColor(COLOR_CHROME_BG)
            // Swallow taps so the chrome behind the splash stays inert while it
            // shows (e.g. the address bar can't be focused mid-boot).
            isClickable = true
        }
        val mark = android.widget.ImageView(this).apply {
            setImageResource(R.drawable.epix_mark_white)
            scaleType = android.widget.ImageView.ScaleType.FIT_CENTER
            // A steady continuous spin (compositor-driven), one turn every
            // 1.2s, for as long as the node is coming up.
            startAnimation(
                android.view.animation.RotateAnimation(
                    0f,
                    360f,
                    android.view.animation.Animation.RELATIVE_TO_SELF,
                    0.5f,
                    android.view.animation.Animation.RELATIVE_TO_SELF,
                    0.5f,
                ).apply {
                    duration = 1200
                    repeatCount = android.view.animation.Animation.INFINITE
                    interpolator = android.view.animation.LinearInterpolator()
                },
            )
        }
        overlay.addView(mark, FrameLayout.LayoutParams(dp(96), dp(96), Gravity.CENTER))
        return overlay
    }

    /** Fade the loading splash out and remove it. Idempotent. */
    private fun hideSplash() {
        val overlay = splashOverlay ?: return
        splashOverlay = null
        overlay.animate()
            .alpha(0f)
            .setDuration(250)
            .withEndAction {
                overlay.clearAnimation()
                (overlay.parent as? android.view.ViewGroup)?.removeView(overlay)
            }
            .start()
    }

    /** Turn what the user typed into somewhere to go. */
    private fun navigate(input: String) {
        val t = input.trim()
        if (t.isEmpty()) return
        val url = when {
            // A leading "?" always searches (the Firefox convention), even
            // for something that would otherwise parse as an address.
            t.startsWith("?") -> searchUrl(t.removePrefix("?"))
            t.startsWith("http://") || t.startsWith("https://") -> t
            t.startsWith("epix://") -> {
                val host = t.removePrefix("epix://").substringBefore('/')
                currentDisplay = host
                nodeUrl(host)
            }
            // Only explicit xite addresses go to the resolver: epix1... or
            // something.epix. A bare word is a search, not an implied .epix.
            t.startsWith("epix1") || t.endsWith(".epix") -> {
                currentDisplay = t
                nodeUrl(t)
            }
            // Looks like a clearnet domain: browse it over https.
            t.contains('.') && !t.contains(' ') -> "https://$t"
            // Everything else - bare words, phrases - searches DuckDuckGo.
            else -> searchUrl(t)
        }
        session.loadUri(url)
        addressBar.clearFocus()
        (getSystemService(Context.INPUT_METHOD_SERVICE) as InputMethodManager)
            .hideSoftInputFromWindow(addressBar.windowToken, 0)
    }

    /** A DuckDuckGo search for typed input that is not an address. */
    private fun searchUrl(query: String): String =
        SEARCH_BASE + java.net.URLEncoder.encode(query.trim(), "UTF-8")

    /** Show `talk.epix/…` in the bar, not the local node plumbing. */
    private fun friendlyUrl(url: String): String {
        val prefix = "$NODE_BASE/"
        if (!url.startsWith(prefix)) return url
        val rest = url.removePrefix(prefix).trimEnd('/')
        rest.substringBefore('/').takeIf { it.isNotEmpty() }?.let { currentDisplay = it }
        return rest
    }

    /** Boot the node off the main thread; return the display name to open. */
    private suspend fun bootNode(target: String): String? = withContext(Dispatchers.IO) {
        try {
            val config = NodeConfig(
                dataDir = filesDir.absolutePath,
                target = target,
                uiAddr = "127.0.0.1:$UI_PORT",
                torMode = "enable",
                version = BuildConfig.VERSION_NAME,
            )
            node.start(config)
            if (node.state() == NodeState.SERVING) target else null
        } catch (e: Exception) {
            // Field reports of a blank first screen were undiagnosable while
            // this was swallowed; the error page + logcat now carry it.
            android.util.Log.e("EpixNode", "node start failed", e)
            null
        }
    }

    /**
     * Poll the node's UI port until it accepts a TCP connection, up to ~15s -
     * the mobile counterpart of the desktop launcher's wait_for_port. Returns
     * whether the port came up.
     */
    private suspend fun awaitUiPort(): Boolean = withContext(Dispatchers.IO) {
        repeat(60) {
            val up = runCatching {
                java.net.Socket().use {
                    it.connect(java.net.InetSocketAddress("127.0.0.1", UI_PORT), 250)
                }
            }.isSuccess
            if (up) return@withContext true
            delay(250)
        }
        false
    }

